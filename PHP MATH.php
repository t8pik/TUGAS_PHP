<?php
// 1. PHP pi() Function
echo "Nilai pi: " . pi() . "<br>";

// 2. PHP min() and max() Functions
echo "Nilai terkecil: " . min(3, 5, 1, 7) . "<br>";
echo "Nilai terbesar: " . max(3, 5, 1, 7) . "<br>";

// 3. PHP abs() Function
echo "Nilai absolut dari -10: " . abs(-10) . "<br>";

// 4. PHP sqrt() Function
echo "Akar kuadrat dari 16: " . sqrt(16) . "<br>";

// 5. PHP round() Function
echo "Pembulatan dari 3.6: " . round(3.6) . "<br>";

// 6. Random Numbers
echo "Angka acak: " . rand() . "<br>";
echo "Angka acak antara 1 dan 100: " . rand(1, 100) . "<br>";
?>
