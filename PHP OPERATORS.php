<?php
$x = 10;
$y = 3;

// 1. Addition (+)
$hasilTambah = $x + $y;
echo "Penjumlahan: $x + $y = $hasilTambah<br>";

// 2. Subtraction (-)
$hasilKurang = $x - $y;
echo "Pengurangan: $x - $y = $hasilKurang<br>";

// 3. Multiplication (*)
$hasilKali = $x * $y;
echo "Perkalian: $x * $y = $hasilKali<br>";

// 4. Division (/)
$hasilBagi = $x / $y;
echo "Pembagian: $x / $y = $hasilBagi<br>";

// 5. Modulus (%)
$hasilMod = $x % $y;
echo "Sisa bagi: $x % $y = $hasilMod<br>";

// 6. Exponentiation (**)
$hasilPangkat = $x ** $y;
echo "Pangkat: $x ** $y = $hasilPangkat<br>";
?>
